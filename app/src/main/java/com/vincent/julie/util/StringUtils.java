	/**
	 * 判断输入的号码是否为合法的电话号码（包括固话，区号，手机）
	 * @param isPhoneNumber
	 * @return
     */
	public static boolean isPhoneNumber(String isPhoneNumber){
		Pattern p = Pattern.compile("((\\d{11})|^((\\d{7,8})|(\\d{4}|\\d{3})-(\\d{7,8})|(\\d{4}|\\d{3})-(\\d{7,8})-(\\d{4}|\\d{3}|\\d{2}|\\d{1})|(\\d{7,8})-(\\d{4}|\\d{3}|\\d{2}|\\d{1}))$)");
		Matcher m = p.matcher(isPhoneNumber);
		return m.matches();
	}
	
